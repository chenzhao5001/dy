package com.guidesound.models;

public class CourseExamine {

    int id;
    int type;
    int uid;
    int item_id;
    int item_sub_type;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public int getItem_id() {
        return item_id;
    }

    public void setItem_id(int item_id) {
        this.item_id = item_id;
    }

    public int getItem_sub_type() {
        return item_sub_type;
    }

    public void setItem_sub_type(int item_sub_type) {
        this.item_sub_type = item_sub_type;
    }
}
